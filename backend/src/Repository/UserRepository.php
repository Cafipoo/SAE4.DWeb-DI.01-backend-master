    public function paginateAllOrderedByLatest(int $offset, int $count): Paginator
    {
        $query = $this->createQueryBuilder('u')
            ->orderBy('u.joinedDate', 'DESC')
            ->setFirstResult($offset)
            ->setMaxResults($count)
            ->getQuery()
        ;

        return new Paginator($query);
